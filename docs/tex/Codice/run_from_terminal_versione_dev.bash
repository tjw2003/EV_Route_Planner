// LANCIARE IL SITO (VERSIONE DI DEVELOPMENT/SERVE)

// Cambiare directory andando nella root dei file
// del progetto build. In VueJS e la cartella /src
$ cd /indirizzo/alla/directory/src

// Installare un gestore di packages di JavaScript
$ sudo apt-get install yarn
// oppure in alternativa si puo usare npm
$ sudo apt-get install nodejs npm

// Installare tutte le packages necessarie per il progetto.
$ yarn
// oppure
$ yarn add
// o
$ npm install

// Lanciare il sito mediante il commando
$ yarn serve
// oppure
$ npm run serve

// Sfogliare la pagina nell'indirizzo
$ firefox localhost:8080/